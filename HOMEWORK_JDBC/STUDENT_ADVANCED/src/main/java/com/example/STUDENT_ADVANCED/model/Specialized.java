package com.example.STUDENT_ADVANCED.model;

public class Specialized {
    private int id;
    private String name;

    public Specialized() {
    }

    public Specialized(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
